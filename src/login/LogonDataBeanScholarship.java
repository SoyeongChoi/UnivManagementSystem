package login;

public class LogonDataBeanScholarship {
	private String scholar_code;
	private int semester;
	private String id;
	private String scholar_name;
	private String scholar_money;
	
	public String getScholar_code() {
		return scholar_code;
	}
	public void setScholar_code(String scholar_code) {
		this.scholar_code = scholar_code;
	}
	public int getSemester() {
		return semester;
	}
	public void setSemester(int semester) {
		this.semester = semester;
	}
	public String getScholar_name() {
		return scholar_name;
	}
	public void setScholar_name(String scholar_name) {
		this.scholar_name = scholar_name;
	}
	public String getScholar_money() {
		return scholar_money;
	}
	public void setScholar_money(String scholar_money) {
		this.scholar_money = scholar_money;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
	
}
